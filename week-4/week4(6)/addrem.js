let numbers = [1, 2, 3];

// Adding elements
numbers.push(4, 5);
console.log(numbers); // Output: [1, 2, 3, 4, 5]

// Removing elements
let removedElement = numbers.pop();
console.log(numbers); // Output: [1, 2, 3, 4]
console.log("Removed element:", removedElement); // Output: Removed element: 5